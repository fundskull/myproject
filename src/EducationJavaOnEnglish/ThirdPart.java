package EducationJavaOnEnglish;

//how to swap two values;

public class ThirdPart {
    public static void main(String[] args) {

        String x = "water";
        String y = "Kool_Aid";
        String temp;

        temp = x;
        x = y;
        y = temp;


        System.out.println("x: " + x);
        System.out.println("y: " + y);
    }
}
